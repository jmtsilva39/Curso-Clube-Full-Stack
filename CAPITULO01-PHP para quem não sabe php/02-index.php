

//Qual a diferença entre include e require ? 


<h1>Include </h1>
<p>Mostra a mensagem de Erro mas continua executando o resto do arquivo.</p>



<h1>Require</h1>
<p>Para e não executa mais nada.</p>

<h1>Require Once ou Include_Once</h1>
<p>Garante que o arquivo vai ser incluído somente uma vez.</p>